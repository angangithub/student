<template>
  <el-dialog
    :title="!dataForm.id ? '新增' : '修改'"
    :close-on-click-modal="false"
    :visible.sync="visible">
    <el-form :model="dataForm" :rules="dataRule" ref="dataForm" @keyup.enter.native="dataFormSubmit()" label-width="80px">
    <el-form-item label="" prop="studentId">
      <el-input v-model="dataForm.studentId" placeholder=""></el-input>
    </el-form-item>
    <el-form-item label="" prop="courseId">
      <el-input v-model="dataForm.courseId" placeholder=""></el-input>
    </el-form-item>
    <el-form-item label="" prop="enrollDate">
      <el-input v-model="dataForm.enrollDate" placeholder=""></el-input>
    </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">取消</el-button>
      <el-button type="primary" @click="dataFormSubmit()">确定</el-button>
    </span>
  </el-dialog>
</template>

<script>
  export default {
    data () {
      return {
        visible: false,
        dataForm: {
          id: 0,
          studentId: '',
          courseId: '',
          enrollDate: ''
        },
        dataRule: {
          studentId: [
            { required: true, message: '不能为空', trigger: 'blur' }
          ],
          courseId: [
            { required: true, message: '不能为空', trigger: 'blur' }
          ],
          enrollDate: [
            { required: true, message: '不能为空', trigger: 'blur' }
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.id = id || 0
        this.visible = true
        this.$nextTick(() => {
          this.$refs['dataForm'].resetFields()
          if (this.dataForm.id) {
            this.$http({
              url: this.$http.adornUrl(`/egroup/enrollments/info/${this.dataForm.id}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.studentId = data.enrollments.studentId
                this.dataForm.courseId = data.enrollments.courseId
                this.dataForm.enrollDate = data.enrollments.enrollDate
              }
            })
          }
        })
      },
      // 表单提交
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/egroup/enrollments/${!this.dataForm.id ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'id': this.dataForm.id || undefined,
                'studentId': this.dataForm.studentId,
                'courseId': this.dataForm.courseId,
                'enrollDate': this.dataForm.enrollDate
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: '操作成功',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
              }
            })
          }
        })
      }
    }
  }
</script>
